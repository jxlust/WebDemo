// topbar推送消息
function topbarMsg() {
    try{
        var exp = new Date();
        exp.setMinutes(exp.getMinutes() + 30);
        var msgNum;
        if (!$.cookie('topbarMsg')) {
            var host = objSite.user_domain;
        	if(window.location.href.indexOf(host) > -1){
				$.post('/user/msg_num',{},function(res){
                    msgNum = res.data.msg_counts;
                    if (msgNum == 0) {
                        $('.topbar-num-dot').hide();
                    } else if (msgNum <= 99) {
                        $('.topbar-num-dot').show();
                    } else {
                        $('.topbar-num-dot').show();
                        msgNum = '99+';
                    }
                    $('.topbar-num-dot').text(msgNum);
                    $.cookie('topbarMsg', msgNum, {expires: exp, domain: '3d66.com', path: '/'});
				});
			}else{
                objSite.ajaxUser('/user/msg_num', {}, function(res){
                    msgNum = res.data.msg_counts;
                    if (msgNum == 0) {
                        $('.topbar-num-dot').hide();
                    } else if (msgNum <= 99) {
                        $('.topbar-num-dot').show();
                    } else {
                        $('.topbar-num-dot').show();
                        msgNum = '99+';
                    }
                    $('.topbar-num-dot').text(msgNum);
                    $.cookie('topbarMsg', msgNum, {expires: exp, domain: '3d66.com', path: '/'});
                });
			}
        } else {
            var oldMsgNum = $.cookie('topbarMsg');
            $('.topbar-num-dot').text(oldMsgNum);
            if (oldMsgNum == 0) {
                $('.topbar-num-dot').hide();
            } else {
                $('.topbar-num-dot').show();
            }
        }
    } catch(e) {}
}

$(function(){
    // topbar消息条数
    topbarMsg();
});